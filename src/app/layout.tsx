import '@/app/globals.css';
import type { Metadata } from 'next';
import type { JSX, ReactNode } from 'react';

export const metadata: Metadata = {
  title: 'Budget Manager',
  description: 'Generated by Erick',
};

const themeScript = `
(() => {
  const themes = ['latte', 'macchiato'];
  const storedTheme = localStorage.getItem('budget-manager-theme');

  document.documentElement.dataset.theme = themes.includes(storedTheme)
    ? storedTheme
    : 'macchiato';
})();
`;

export default function RootLayout({
  children,
}: Readonly<{
  children: ReactNode;
}>): JSX.Element {
  return (
    <html className="h-full" lang="en" suppressHydrationWarning>
      <head>
        <script dangerouslySetInnerHTML={{ __html: themeScript }} />
      </head>
      <body
        className="min-h-screen text-[var(--ctp-text)] antialiased transition-colors"
        suppressHydrationWarning
      >
        {children}
      </body>
    </html>
  );
}
